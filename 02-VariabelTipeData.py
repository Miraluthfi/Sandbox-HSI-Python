"""Variabel dan Tipe int, float, string
 Variabel nilainya bisa berubah-ubah"""

# tipe data integer
price = 100000
print(price)

# tipe data float
discount = 0.5
print(discount)

price = price * discount
print(price)

# tipe data string
product_name = "Gundam"
print(product_name)

# tipe data boolean
is_discount = False
print(is_discount)